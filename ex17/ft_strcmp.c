int		ft_strcmp(char *s1, char *s2)
{
	unsigned int		i;
	char				c;
	int					dif;

	i = 0;
	dif = 0;
	while (s1[i] == s2[i] && s1[i] != '\0')
		i++;
	c = s2[i];
	if (s1[i] > c)
		while (s1[i] != c)
		{
			c++;
			dif++;
		}
	else
		while (s1[i] != c)
		{
			c--;
			dif--;
		}
	return (dif);
}
